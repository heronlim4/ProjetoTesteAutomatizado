package org.ClassesDoSistema;

public class Calculadora {
    public int somar(int a, int b) {
        return a + b;
    }

    public int subtrair(int a, int b) {
        return a - b;
    }

    public boolean e_par(int number) {
        return number % 2 == 0;
    }

    public float dividir(float a, float b) {
        return a/b;
    }
}
